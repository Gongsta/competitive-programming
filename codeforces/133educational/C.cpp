#include <bits/stdc++.h>

typedef long long ll;

using namespace std;


int main() {
    ios::sync_with_stdio(0);
    cin.tie(0);
    
    int t;
    cin >> t;
    while (t--) {
        // 3 ways
        int m;
        cin >> m;
        ll a[2][m];
        for (int i=0;i<2;i++) {
            for (int j=0;j<m;j++) {
                cin >> a[i][j];
            }
        }
    }

    return 0;
}
